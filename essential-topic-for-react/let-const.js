// let allows you to declare variables that can be reassigned.
// Variables declared with let have block scope, which means they are only accessible within the block (enclosed by curly braces {}) where they are declared. They are not hoisted to the top of their containing function or block and are not accessible before the declaration.
// You can declare a let variable without initializing it, and then assign a value later.

//const variable


//const is used to declare variables whose values should not be reassigned after their initial assignment.
// Variables declared with const also have block scope like let.
// You must initialize a const variable at the time of declaration, and you cannot change its value afterward.