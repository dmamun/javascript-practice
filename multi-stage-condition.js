// Example of finding letter grade without using a function
var numberGrade = 85;

if (numberGrade >= 90) {
  console.log("A");
} else if (numberGrade >= 80) {
  console.log("B");
} else if (numberGrade >= 70) {
  console.log("C");
} else if (numberGrade >= 60) {
  console.log("D");
} else {
  console.log("F");
}
